function OrionLib:Init()
	if OrionLib.SaveCfg then
		task.defer(function()
			pcall(function()
				local path = OrionLib.Folder .. "/" .. tostring(game.GameId) .. ".json"
				if isfile(path) then
					local raw = readfile(path)
					if raw and raw ~= "" then
						LoadCfg(raw)
						OrionLib:MakeNotification({
							Name = "Config Loaded",
							Content = "Saved configuration applied.",
							Time = 3
						})
					end
				end
			end)
		end)
	end
end

